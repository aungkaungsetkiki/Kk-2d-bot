async def handle_message(update: Update, context: ContextTypes.DEFAULT_TYPE):
    try:
        user = update.effective_user
        text = update.message.text
        
        if not user or not user.username:
            await update.message.reply_text("‚ùå ·ÄÄ·Äª·Ä±·Ä∏·Äá·Ä∞·Ä∏·Äï·Äº·ÄØ·Åç Telegram username ·Äû·Äê·Ä∫·Äô·Äæ·Äê·Ä∫·Äï·Ä´")
            return

        key = get_current_date_key()
        if not date_control.get(key, False):
            await update.message.reply_text("‚ùå ·ÄÖ·Ä¨·Äõ·ÄÑ·Ä∫·Ä∏·Äï·Ä≠·Äê·Ä∫·Äë·Ä¨·Ä∏·Äï·Ä´·Äû·Ää·Ä∫")
            return

        if not text:
            await update.message.reply_text("‚ö†Ô∏è ·Äô·ÄÄ·Ä∫·ÄÜ·Ä±·Ä∑·ÄÇ·Äª·Ä∫·Äô·Äõ·Äæ·Ä≠·Äï·Ä´")
            return

        # Check for invalid characters
        if any(c in text for c in ['%', '&', '*', '$']):
            await update.message.reply_text("‚ö†Ô∏è ·Äô·Äæ·Ä¨·Ä∏·Äî·Ä±·Äï·Ä´·Äê·Äö·Ä∫\n·Ä°·Äë·Ä∞·Ä∏·Äû·ÄÑ·Ä∫·Äπ·ÄÄ·Ä±·Äê·Äô·Äª·Ä¨·Ä∏ (%&*$) ·Äô·Äï·Ä´·Äõ·Äï·Ä´\n·Ä•·Äï·Äô·Ä¨: 12-500")
            return

        entries = text.split()
        bets = []
        total_amount = 0

        i = 0
        while i < len(entries):
            entry = entries[i]
            
            # Case 1: Simple number-amount (12-1000)
            if '-' in entry and 'r' not in entry:
                parts = entry.split('-')
                if len(parts) == 2 and parts[0].isdigit() and parts[1].isdigit():
                    num = int(parts[0])
                    amt = int(parts[1])
                    if 0 <= num <= 99:
                        bets.append(f"{num:02d}-{amt}")
                        total_amount += amt
                        i += 1
                        continue
            
            # Case 2: Reverse format (12r1000)
            if 'r' in entry and '-' not in entry:
                parts = entry.split('r')
                if len(parts) == 2 and parts[0].isdigit() and parts[1].isdigit():
                    num = int(parts[0])
                    amt = int(parts[1])
                    rev = reverse_number(num)
                    if 0 <= num <= 99:
                        bets.append(f"{num:02d}-{amt}")
                        bets.append(f"{rev:02d}-{amt}")
                        total_amount += amt * 2
                        i += 1
                        continue
            
            # Case 3: Combined format (23-1000r500)
            if 'r' in entry and '-' in entry:
                main_part, r_part = entry.split('r', 1)
                if '-' in main_part:
                    num_part, amt_part = main_part.split('-')
                    if num_part.isdigit() and amt_part.isdigit() and r_part.isdigit():
                        num = int(num_part)
                        amt1 = int(amt_part)
                        amt2 = int(r_part)
                        rev = reverse_number(num)
                        if 0 <= num <= 99:
                            bets.append(f"{num:02d}-{amt1}")
                            bets.append(f"{rev:02d}-{amt2}")
                            total_amount += amt1 + amt2
                            i += 1
                            continue
            
            # Case 4: Number followed by reverse (48 2000r1000)
            if entry.isdigit() and i+1 < len(entries) and 'r' in entries[i+1]:
                num = int(entry)
                r_part = entries[i+1]
                if r_part.count('r') == 1:
                    amt_part1, amt_part2 = r_part.split('r')
                    if amt_part1.isdigit() and amt_part2.isdigit():
                        amt1 = int(amt_part1)
                        amt2 = int(amt_part2)
                        rev = reverse_number(num)
                        if 0 <= num <= 99:
                            bets.append(f"{num:02d}-{amt1}")
                            bets.append(f"{rev:02d}-{amt2}")
                            total_amount += amt1 + amt2
                            i += 2
                            continue
            
            # Case 5: Group bets (123·Ä°·ÄÅ·ÄΩ·Ä±1000)
            if '·Ä°·ÄÅ·ÄΩ·Ä±' in entry or '·Ä°·Äï·Ä∞·Ä∏·Äï·Ä´·Ä°·ÄÅ·ÄΩ·Ä±' in entry:
                base = entry.replace('·Ä°·ÄÅ·ÄΩ·Ä±', '').replace('·Ä°·Äï·Ä∞·Ä∏·Äï·Ä´', '')
                if base.isdigit() and len(base) >= 2:
                    digits = [int(d) for d in base]
                    pairs = []
                    # Generate all unique 2-digit combinations
                    for j in range(len(digits)):
                        for k in range(len(digits)):
                            if j != k:
                                combo = digits[j] * 10 + digits[k]
                                if combo not in pairs:
                                    pairs.append(combo)
                    
                    # Add doubles if ·Ä°·Äï·Ä∞·Ä∏·Äï·Ä´·Ä°·ÄÅ·ÄΩ·Ä±
                    if '·Ä°·Äï·Ä∞·Ä∏·Äï·Ä´·Ä°·ÄÅ·ÄΩ·Ä±' in entry:
                        for d in digits:
                            double = d * 10 + d
                            if double not in pairs:
                                pairs.append(double)
                    
                    if i+1 < len(entries) and entries[i+1].isdigit():
                        amt = int(entries[i+1])
                        for num in pairs:
                            bets.append(f"{num:02d}-{amt}")
                            total_amount += amt
                        i += 2
                        continue
            
            # Case 6: Special combinations (·Ä°·Äï·Ä∞·Ä∏, ·Äï·Ä´·Äù·Ä´, etc.)
            fixed_special_cases = {
                "·Ä°·Äï·Ä∞·Ä∏": [0, 11, 22, 33, 44, 55, 66, 77, 88, 99],
                "·Äï·Ä´·Äù·Ä´": [5, 16, 27, 38, 49, 50, 61, 72, 83, 94],
                "·Äî·ÄÄ·Äπ·ÄÅ": [7, 18, 24, 35, 42, 53, 69, 70, 81, 96],
                "·Ää·ÄÆ·ÄÄ·Ä≠·ÄØ": [1, 12, 23, 34, 45, 56, 67, 78, 89, 90],
                "·ÄÄ·Ä≠·ÄØ·Ää·ÄÆ": [9, 10, 21, 32, 43, 54, 65, 76, 87, 98],
            }
            
            if entry in fixed_special_cases:
                if i+1 < len(entries) and entries[i+1].isdigit():
                    amt = int(entries[i+1])
                    for num in fixed_special_cases[entry]:
                        bets.append(f"{num:02d}-{amt}")
                        total_amount += amt
                    i += 2
                    continue
            
            # Case 7: Position-based bets (·Äë·Ä≠·Äï·Ä∫, ·Äï·Ä≠·Äê·Ä∫, etc.)
            dynamic_types = ["·Äë·Ä≠·Äï·Ä∫", "·Äï·Ä≠·Äê·Ä∫", "·Äò·Äõ·Ä≠·Äê·Ä∫", "·Ä°·Äï·Ä´"]
            found_dynamic = False
            for dtype in dynamic_types:
                if entry.endswith(dtype):
                    prefix = entry[:-len(dtype)]
                    if prefix.isdigit():
                        digit_val = int(prefix)
                        if 0 <= digit_val <= 9:
                            numbers = []
                            if dtype == "·Äë·Ä≠·Äï·Ä∫":
                                numbers = [digit_val * 10 + j for j in range(10)]
                            elif dtype == "·Äï·Ä≠·Äê·Ä∫":
                                numbers = [j * 10 + digit_val for j in range(10)]
                            elif dtype == "·Äò·Äõ·Ä≠·Äê·Ä∫":
                                numbers = [n for n in range(100) if (n//10 + n%10) % 10 == digit_val]
                            elif dtype == "·Ä°·Äï·Ä´":
                                tens = [digit_val * 10 + j for j in range(10)]
                                units = [j * 10 + digit_val for j in range(10)]
                                numbers = list(set(tens + units))
                            
                            if i+1 < len(entries) and entries[i+1].isdigit():
                                amt = int(entries[i+1])
                                for num in numbers:
                                    bets.append(f"{num:02d}-{amt}")
                                    total_amount += amt
                                i += 2
                                found_dynamic = True
                            break
            if found_dynamic:
                continue
            
            # Case 8: Simple number with default amount (12)
            if entry.isdigit():
                num = int(entry)
                if 0 <= num <= 99:
                    # Check if next entry is a number (amount)
                    if i+1 < len(entries) and entries[i+1].isdigit():
                        amt = int(entries[i+1])
                        bets.append(f"{num:02d}-{amt}")
                        total_amount += amt
                        i += 2
                    else:
                        # Default amount if not specified
                        bets.append(f"{num:02d}-500")
                        total_amount += 500
                        i += 1
                    continue
            
            i += 1  # Skip unrecognized entries

        # Store the bets
        if user.username not in user_data:
            user_data[user.username] = {}
        if key not in user_data[user.username]:
            user_data[user.username][key] = []

        for bet in bets:
            num, amt = bet.split('-')
            num = int(num)
            amt = int(amt)
            ledger[num] = ledger.get(num, 0) + amt
            user_data[user.username][key].append((num, amt))

        # Send confirmation
        if bets:
            response = "\n".join(bets) + f"\n·ÄÖ·ÄØ·ÄÖ·ÄØ·Äï·Ä±·Ä´·ÄÑ·Ä∫·Ä∏ {total_amount} ·ÄÄ·Äª·Äï·Ä∫"
            await update.message.reply_text(response)
        else:
            await update.message.reply_text("‚ö†Ô∏è ·Ä°·ÄÅ·Äª·ÄÄ·Ä∫·Ä°·Äú·ÄÄ·Ä∫·Äô·Äª·Ä¨·Ä∏·ÄÄ·Ä≠·ÄØ·ÄÖ·ÄÖ·Ä∫·ÄÜ·Ä±·Ä∏·Äï·Ä´")
            
    except Exception as e:
        logger.error(f"Error in handle_message: {str(e)}")
        await update.message.reply_text(f"‚ùå Error: {str(e)}")

